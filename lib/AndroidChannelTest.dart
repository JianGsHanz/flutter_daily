class _ChannelRoute extends StatefulWidget {
  @override
  ChannelState createState() => ChannelState();
}

class ChannelState extends State<_ChannelRoute> {
  //我们建议在通道名称前加一个唯一的“域名前缀”
  static MethodChannel platform = const MethodChannel('zyh.flutter.io/battery');

  String _batteryLevel = 'Unknown battery level.';

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Column(
        children: [
          Padding(padding: EdgeInsets.only(top: 48.0)),
          RaisedButton(
            onPressed: _getBatteryLevel,
            child: Text("获取电池电量"),
          ),
          Text(_batteryLevel),
        ],
      ),
    );
  }

  Future<Null> _getBatteryLevel() async {
    String batteryLevel;
    try {
      // 在通道上调用此方法
      final int result = await platform.invokeMethod('getBatteryLevel');
      batteryLevel = 'Battery level at $result % .';
    } on PlatformException catch (e) {
      batteryLevel = "Failed to get battery level: '${e.message}'.";
    }

    setState(() {
      _batteryLevel = batteryLevel;
    });
  }
}

